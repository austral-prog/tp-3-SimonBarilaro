def check_vowels():
    nombre = input("")
    print("Contiene a: " + str("a" in nombre.lower()))
    print("Contiene e: " + str("e" in nombre.lower()))
    print("Contiene i: " + str("i" in nombre.lower()))
    print("Contiene o: " + str("o" in nombre.lower()))
    print("Contiene u: " + str("u" in nombre.lower()))
